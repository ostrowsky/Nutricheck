class DeficitAnalyzer:
    @staticmethod
    def analyze(calculated_data):
        """
        Находит дефициты (покрытие < 80%) и генерирует рекомендации продуктов
        """
        nutrients = calculated_data['nutrients']
        recommendations = []

        exclude = ['calories', 'protein', 'fat', 'carbs', 'fiber', 'sodium']

        for key, value in nutrients.items():
            if key in exclude:
                continue

            percent = value['percent']
            if percent >= 80:
                continue

            desc_info = NUTRIENT_DESCRIPTIONS.get(key)
            if not desc_info:
                continue

            recommendations.append({
                'key': key,
                'name': value['name'],
                'icon': desc_info['icon'],
                'percent': percent,
                'desc': desc_info['desc'],
                'foods': desc_info['foods'],
                'severity': 'critical' if percent < 50 else 'moderate'
            })

        # Сортируем: критические сначала, затем по проценту по возрастанию
        return sorted(recommendations, key=lambda x: (0 if x['severity'] == 'critical' else 1, x['percent']))
